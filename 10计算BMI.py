# 【知识点】BMI计算与条件判断
# BMI = 体重(kg) / 身高(m)²
# 中国成人BMI标准：18.5-23.9为正常

height=float(input("请输入你的身高（厘米）："))                 # 输入身高（厘米），转换为浮点数
weight=float(input("请输入你的体重（千克）："))                 # 输入体重（千克），转换为浮点数
BMI=weight/((height/100)**2)                                   # 计算BMI：体重÷(身高米)²，height/100将厘米转为米
if 18.5<=BMI<24:                                                # 判断BMI是否在正常范围内（18.5≤BMI<24）
    print("你的体重指数BMI为：",'%.2f'%BMI,"符合标准！")        # 格式化输出BMI值，保留2位小数
else:
    print("你的体重指数BMI为：",'%.2f'%BMI,"不符合标准！")      # BMI不在正常范围
