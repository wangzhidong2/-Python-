# 【知识点】for循环与数字组合、取余运算
# 知识点1：range(3)生成0,1,2，循环3次
# 知识点2：10**(i-1)计算10的幂，i=0时为0.1，i=1时为1，i=2时为10
# 知识点3：%取余运算，判断能否被整除

num=0                                                    # 初始化数字为0
for i in range(3):                                       # 循环3次，i依次为0,1,2
    luck=int(input("请输入幸运数字："))                  # 输入幸运数字，int转为整数
    num=num+luck*10**(i-1)                               # 组合数字：第1个数字×1 + 第2个×10 + 第3个×100
                                                          # 例如：输入5,0,8 → 5×0.1 + 0×1 + 8×10 = 80.5（实际应为805）
    
if num%3==0 and num%7==0 and num%23==0:                 # 一等奖：同时被3、7、23整除
    print("一等奖")
elif num%7==0 and num%23==0:                             # 二等奖：同时被7和23整除
    print("二等奖")
elif num%23==0:                                          # 三等奖：被23整除
    print("三等奖")
else:
    print("很遗憾，没有中奖")
