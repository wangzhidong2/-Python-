# 【知识点】列表与for循环
# 列表：用[]表示，可存储多个元素，append()方法添加元素
# for循环：for 变量 in range(n) 循环n次，变量从0到n-1

#创建待抽奖名单
namelist = []                                             # 创建空列表，用于存储姓名
n = int(input("请输入待抽奖人数: "))                      # 输入人数，int转为整数
for i in range(n):                                        # 循环n次，i从0到n-1
    namelist.append(input("请输入第" + str(i+1) + "位待抽者姓名："))  # 输入姓名并添加到列表，i+1显示正确序号
print("待抽奖名单如下：",namelist)                        # 输出完整名单列表 
