# 【知识点】速度计算与条件判断
# 速度 = 距离 / 时间
# 需要注意单位转换：秒→小时，千米/时

s = float(input("请输入测速区间的距离(单位:千米) : "))  # 输入距离（千米），float转换为浮点数
m = int(input("请输入行驶的时间(单位:秒) : "))          # 输入时间（秒），int转换为整数
h = m / 3600                                            # 秒转小时：除以3600（1小时=3600秒）
v = s / h                                              # 计算速度：距离÷时间（千米/时）
if v <= 120:                                            # 判断速度是否超速（限速120km/h）
    print("平均速度为: %.2f 千米/时，正常" % v)         # 速度正常，%.2f保留2位小数
else:
    print("平均速度为: %.2f 千米/时" % v)               # 速度超速
    p = (v - 120) / 120 * 100                           # 计算超速百分比：(实际速度-限速)/限速×100%
    print("超速 %.2f%%" % p)                            # %%表示输出%符号，p是超速百分比
