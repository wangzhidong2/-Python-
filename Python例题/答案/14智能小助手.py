# 【知识点】多分支条件判断（if-elif-else）
# 注意：elif是else if的缩写，用于多条件判断
# 条件判断顺序很重要，从上到下依次判断

s = int(input("请输入某成人的心率（单位：次/分钟）: "))  # 输入心率，int转为整数
if 60 > s > 45:                                          # 判断：45 < 心率 < 60（窦性心动过缓）
    print("窦性心动过缓")
elif 60 <= s <= 100:                                     # 判断：60 ≤ 心率 ≤ 100（正常心率）
    print("正常心率")
elif 160 > s > 100:                                      # 判断：100 < 心率 < 160（窦性心动过速）
    print("窦性心动过速")
else:
    print("输入错误！")                                  # 其他情况（心率≤45或≥160）
