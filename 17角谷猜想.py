# 【知识点】while循环与条件判断
# 角谷猜想：任意正整数，经过以下运算最终会变成1
#   - 如果是偶数，除以2
#   - 如果是奇数，乘以3再加1
# 知识点：while循环、if-else、//整除、%取余

n=int(input("请输入一个正整数："))                        # 输入正整数，int转为整数
while n>1:                                               # 当n>1时继续循环
   if n%2==0:                                            # 判断n是否为偶数（n除以2余数为0）
         n=n//2                                          # 偶数：整除2（//是整除运算符）
   else:                                                
       n=3*n+1                                           # 奇数：乘以3再加1
print(int(n))                                            # 输出最终结果（应为1）                            
